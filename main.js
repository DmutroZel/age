let age = prompt('Введи свій вік');




if ( age < 5 ) {
    alert('Тобі ще в садок треба!');
}else if ( age < 17){
    alert('Йди до школи!');
}else if ( age < 24 ){
    alert('Йди в університет!'); 
}else if ( age < 55){
    alert( 'Тобі завтра на роботу!');
}else if ( age < 93){
    alert('Ти вже пенсіонер');
}else if ( age < 100000000000){
    alert('Мені здається ти маєш бути вже в гробу');
}
     